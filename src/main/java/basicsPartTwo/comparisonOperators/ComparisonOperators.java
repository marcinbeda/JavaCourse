public class ComparisonOperators {

    public static void main(String[] args) {
    	boolean exampleVariable;

    	exampleVariable = 5 == 2;
    	exampleVariable = 2 != 2;
    	exampleVariable = 2 > 2;
    	exampleVariable = 0 < 2;
    	exampleVariable = 2 >= 2;
    	exampleVariable = 1 <= 2;

		System.out.println(exampleVariable);
    }
}
